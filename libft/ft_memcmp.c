#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, unsigned long int n)
{
	while (n)
	{
		if (*(char *)s1 != *(char *)s2)
			return (*(unsigned char *)s1 - *(unsigned char *)s2);
		s1++;
		s2++;
		n--;
	}
	return (0);
}
